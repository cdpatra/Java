//    -=> in the java programming language, each class is allowed to have one direct superclass,
//        and each superclass has the potential for an unlimited number of subclasses
//        (MULTIPLE INHERITANCE IS NOT ALLOWED IN JAVA)
//    -=> private members of the superclass are not accessible by the subclass and can only be indirectly accessed.
//    -=> members that have default accessibility in the superclass are also not accessible by the subclasses
//        in other packages.

//    JAVA SUPPORTS
//    1. single inheritance
//    2. multilevel inheritance
//    3. hierarchical inheritance
